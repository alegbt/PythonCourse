listEl = ["ciao", "ciao2", "ciao3", "ciao4", ]

print(listEl[1:3]) # ['ciao2', 'ciao3']
print(listEl[1:2]) # ['ciao2']
print(listEl[0:2]) # ['ciao', 'ciao2']
print(listEl[:2]) # ['ciao', 'ciao2']
print(listEl[2:5]) # ['ciao3', 'ciao4']
print(listEl[2:]) # ['ciao3', 'ciao4']
print(listEl[-4:-2]) # ['ciao', 'ciao2'] #parte da -4 fino a -2 non incluso
print(listEl[1:-1]) # ['ciao2', 'ciao3']

print(listEl[::2]) # ['ciao', 'ciao3'] salta ad ogni 2 elementi

